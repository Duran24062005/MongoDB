// Data Estruturada en MongoDB
/*
    ¿Qué son los datos estructurados?

    Los datos estructurados (tambien conocidos como datos relacionales)
    se refiere a datos que se ajustan a un modelo de datos predefinido.
    Se puede mapear fácilmente en campos designados. Un código postal de EE.UU.
    Se puede almacenar como una cadena de 5 dígitos (por ejemplo, 90210), un estado
    como una abreviatura de dos caracteres (por ejemplo, CA), etc.
    Los datos estructurados se almacenan y recuperan fácilmente en una base
    de datos relacional tradicional donde el sistema de gestión aplica la 
    lógica para garantizar que la información esté en el formato
    correcto a medida que se escribe en el disco.
*/
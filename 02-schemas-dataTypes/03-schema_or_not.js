// ¿Schemas o no Schema para la estructura de los documentos?

/*
    Ventajas de Schema fijo:
    1. Datos ordenados.
    2. Se puede realizar un layout o documentación.
    3. Más fácil realizar busquedas.
    4. Sencillez al momento de compartir los datos
    5. Conocer el limite de nuestros datos (datos limpios).

    Ventajas de Schenas no fijos:
    1. Es más versatil frente a los cambios.
    2. Los cambios en la estructura se realizán más rapido.
    3. Se puede pasar al Schema fijo sin dificultades.

    Finalmente, una cosa importante a considerar cuando cuando se renuncia a un schema
    es que no es una desición permanente. Si se niega a imponer un schema en el presente,
    la opción aún permanece abierta para usted en el futuro. En cualquier momento si sus datos se vuelven más ordenados
    o si se encuentra que la falta de schema obstaculiza su capacidad para coordinarse
    entre multiples desarrolladores, puede adoptar un schema de manera bastante trivial.
    Del mismo modo, siempre puede abrir las compuertas en una aplicación que anteriormente 
    tenia un schema y permitir que ingrese cualquier tipo de datos.
*/